public class TwoPointOne {
    public static void main(String[] args) {
        int x = 3;
        int y = 4;
        // 加法
        System.out.println("x+y="+(x+y));
        // 减法
        System.out.println("x-y="+(x-y));
        // 乘法
        System.out.println("x*y="+(x*y));
        // 除法
        System.out.println("x/y="+(x/y));
        // 取模
        System.out.println("x%y="+(x%y));
    }
}
